package ds.arrays;

public class FindSingleOccurance {

    public static void main(String[] args) {

        int arr[] = {4, 1, 2, 1, 2};
        int a = 0;
        int n = arr.length;

        System.out.println(5^5);

        for (int i = 0; i < n; i++) {
            a = a^arr[i];
        }
        System.out.println(a);
    }

}
